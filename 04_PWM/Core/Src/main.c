int main(void)
{
  /* USER CODE BEGIN 1 */

	// В графическом интерфейсе включить таймер (3) и выбрать один из 4 каналов (1),
	// поставить его в режим Output Compare CH (Mode - Toggle on match).
	// Со таймером 2 по непонятной причине не пошло.
	// В значении делителя поставить что-нибудь кратное частоте и не слишком большое (16МГц -> 160).
	// Период взят 100 для представления в процентах.
	// Включить прерывания таймера и EXTI line 0.
	// Тактирование включать не требуется.
	// Соединить перемычкой пин канала таймера (PA6) и пин диода (PD13 / PG13).

	// LED в режим input (по умолчанию)
	GPIOD->MODER &= ~(3 << (2 * 13)); // PD13 to 00
	GPIOA->MODER &= ~3; // PA0 to 00
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_TIM3_Init();
  /* USER CODE BEGIN 2 */
	TIM3->CCER |= TIM_CCER_CC1E; // Включить вывод 1 канала
	TIM3->CCR1 = brightness; // Средняя яркость вначале
	TIM3->CR1 |= TIM_CR1_CEN; // Включить таймер
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
	while (1) {
		if (button_CNT > 50) {
			// Переключить в режим уменьшения/добавления яркости
			if (brightness + step >= TIM3->ARR || brightness < 0)
				step *= -1;
			// Прибавить шаг якрости
			brightness += step;
			// Сбросить кнопку
			button_CNT = 0;
			// Установить скважность
			TIM3->CCR1 = brightness;
		}
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
	}
  /* USER CODE END 3 */
}

